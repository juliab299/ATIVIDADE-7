let palavra = prompt("Digite uma palavra: ");
let palindromo = true;
for (let i = 0; i < palavra.length / 2; i++) {
    if (palavra[i] !== palavra[palavra.length - 1 - i]) {
        palindromo = false;
        break;
    }
}
if (palindromo) {
    console.log("${palavra} é um palíndromo.");
} else {
    console.log("${palavra} não é um palíndromo.");
}
